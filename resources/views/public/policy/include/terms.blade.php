    <p style="text-align: center;"><span style="font-size:20px"><strong>ĐIỀU KHOẢN
                MUA BÁN HÀNG HÓA THƯƠNG MẠI ĐIỆN TỬ</strong></span></p>
    <p><strong>Giới thiệu</strong></p>
    <p>Chào mừng quý khách đến với Rockylens.com</p>
    <p>Chúng tôi là Công ty TNHH TẦM NHÌN SÁNG VIỆT NAM&nbsp;chuyên sản xuất và phân
        phối độc quyền nhãn hàng tròng kính Rocky Canada Opticians&nbsp;, giao dịch
        qua trang web Rockylens.com và tại địa chỉ Showroom của chúng tôi.</p>
    <p>– Quý khách vui lòng kiểm tra thường xuyên để cập nhật những thay đổi của
        chúng tôi.</p>
    <p><strong>1. Hướng dẫn sử dụng website</strong></p>
    <p>– Khi vào web của chúng tôi, người dùng tối thiểu phải 18 tuổi hoặc truy cập
        dưới sự giám sát của cha mẹ hay người giám hộ hợp pháp.</p>
    <p>– Chúng tôi cấp giấy phép sử dụng để bạn có thể mua sắm trên web trong khuôn
        khổ Điều khoản và Điều kiện sử dụng đã đề ra.</p>
    <p>– Nghiêm cấm sử dụng bất kỳ phần nào của trang web này với mục đích thương
        mại hoặc nhân danh bất kỳ đối tác thứ ba nào nếu không được chúng tôi cho
        phép bằng văn bản. Nếu vi phạm bất cứ điều nào trong đây, chúng tôi sẽ hủy
        giấy phép của bạn mà không cần báo trước.</p>
    <p>– Trang web này chỉ dùng để cung cấp thông tin sản phẩm chứ chúng tôi không
        phải nhà sản xuất nên những nhận xét hiển thị trên web là ý kiến cá nhân của
        khách hàng, không phải của chúng tôi.</p>
    <p>– Quý khách phải đăng ký tài khoản với thông tin xác thực về bản thân và phải
        cập nhật nếu có bất kỳ thay đổi nào. Mỗi người truy cập phải có trách nhiệm
        với mật khẩu, tài khoản và hoạt động của mình trên web. Hơn nữa, quý khách
        phải thông báo cho chúng tôi biết khi tài khoản bị truy cập trái phép. Chúng
        tôi không chịu bất kỳ trách nhiệm nào, dù trực tiếp hay gián tiếp, đối với
        những thiệt hại hoặc mất mát gây ra do quý khách không tuân thủ quy định.
    </p>
    <p>– Trong suốt quá trình đăng ký, quý khách đồng ý nhận email quảng cáo từ
        website. Sau đó, nếu không muốn tiếp tục nhận mail, quý khách có thể từ chối
        bằng cách nhấp vào đường link ở dưới cùng trong mọi email quảng cáo.</p>
    <p><strong>2. Ý kiến khách hàng</strong></p>
    <p>Tất cả nội dung trang web và ý kiến phê bình của quý khách đều là tài sản của
        chúng tôi. Nếu chúng tôi phát hiện bất kỳ thông tin giả mạo nào, chúng tôi
        sẽ khóa tài khoản của quý khách ngay lập tức hoặc áp dụng các biện pháp khác
        theo quy định của pháp luật Việt Nam.</p>
    <p><strong>3. Chấp nhận đơn hàng và giá cả</strong></p>
    <p>– Chúng tôi có quyền từ chối hoặc hủy đơn hàng của quý khách vì bất kỳ lý do
        gì vào bất kỳ lúc nào. Chúng tôi có thể hỏi thêm về số điện thoại và địa chỉ
        trước khi nhận đơn hàng.</p>
    <p>– Chúng tôi cam kết sẽ cung cấp thông tin giá cả chính xác nhất cho người
        tiêu dùng. Tuy nhiên, đôi lúc vẫn có sai sót xảy ra, ví dụ như trường hợp
        giá sản phẩm không hiển thị chính xác trên trang web hoặc sai giá, tùy theo
        từng trường hợp chúng tôi sẽ liên hệ hướng dẫn hoặc thông báo hủy đơn hàng
        đó cho quý khách. Chúng tôi cũng có quyền từ chối hoặc hủy bỏ bất kỳ đơn
        hàng nào dù đơn hàng đó đã hay chưa được xác nhận hoặc đã bị thanh toán.</p>
    <p><strong>4. Thương hiệu và bản quyền</strong></p>
    <p>Mọi quyền sở hữu trí tuệ (đã đăng ký hoặc chưa đăng ký), nội dung thông tin
        và tất cả các thiết kế, văn bản, đồ họa, phần mềm, hình ảnh, video, âm nhạc,
        âm thanh, biên dịch phần mềm, mã nguồn và phần mềm cơ bản đều là tài sản của
        chúng tôi. Toàn bộ nội dung của trang web được bảo vệ bởi luật bản quyền của
        Việt Nam và các công ước quốc tế. Bản quyền đã được bảo lưu.</p>
    <p><strong>5. Quyền pháp lý</strong></p>
    <p>Các điều kiện, điều khoản và nội dung của trang web này được điều chỉnh bởi
        luật pháp Việt Nam và Tòa án có thẩm quyền tại Việt Nam sẽ giải quyết bất kỳ
        tranh chấp nào phát sinh từ việc sử dụng trái phép trang web này.</p>
    <p><strong>6. Quy định về bảo mật</strong></p>
    <p>– Trang web của chúng tôi coi trọng việc bảo mật thông tin và sử dụng các
        biện pháp tốt nhất bảo vệ thông tin và việc thanh toán của quý khách. Thông
        tin của quý khách trong quá trình thanh toán sẽ được mã hóa để đảm bảo an
        toàn. Sau khi quý khách hoàn thành quá trình đặt hàng, quý khách sẽ thoát
        khỏi chế độ an toàn.</p>
    <p>– Quý khách không được sử dụng bất kỳ chương trình, công cụ hay hình thức nào
        khác để can thiệp vào hệ thống hay làm thay đổi cấu trúc dữ liệu. Trang web
        cũng nghiêm cấm việc phát tán, truyền bá hay cổ vũ cho bất kỳ hoạt động nào
        nhằm can thiệp, phá hoại hay xâm nhập vào dữ liệu của hệ thống. Cá nhân hay
        tổ chức vi phạm sẽ bị tước bỏ mọi quyền lợi cũng như sẽ bị truy tố trước
        pháp luật nếu cần thiết.</p>
    <p>– Mọi thông tin giao dịch sẽ được bảo mật nhưng trong trường hợp cơ quan pháp
        luật yêu cầu, chúng tôi sẽ buộc phải cung cấp những thông tin này cho các cơ
        quan pháp luật.</p>
    <p><strong>7. Thanh toán an toàn và tiện lợi tại Rockylens.com</strong></p>
    <p>Mọi khách hàng tham giao giao dịch tại Rockylens.com qua thẻ tín dụng quốc tế
        đều được bảo mật thông tin bằng mã hóa (xem chi tiết tại điều khoản “Quy
        định về bảo mật” được nêu ở trên). Qua đó, khi thực hiện thanh toán qua mạng
        quý khách lưu ý các chi tiết sau:</p>
    <p>-Chỉ sử dụng trang web có chứng chỉ thanh toán an toàn.</p>
    <p>-Tuyệt đối không cho người khác mượn thẻ tín dụng hoặc tài khoản của mình để
        thực hiện thanh toán tại Rockylens.com; trong trường hợp phát sinh giao dịch
        ngoài ý muốn, quý khách vui lòng thông báo cho Rockylens.com biết để có thể
        hỗ trợ kịp thời.</p>
    <p>-Kiểm tra tài khoản ngân hàng của mình thường xuyên để đảm bảo tất cả giao
        dịch qua thẻ đều nằm trong tầm kiểm soát.</p>
    <p>-Trong một số trường hợp phát sinh giao dịch bất thường hoặc giá trị đơn hàng
        quá lớn, Rockylens.com có thể sẽ yêu cầu quý khách chụp hình/scan thẻ tín
        dụng và chứng minh nhân dân của chủ thẻ để xác minh tính chính xác và độ
        trung thực của giao dịch. Khi quý khách nhận được yêu cầu này, vui lòng che
        3 số CVV ở mặt sau của thẻ để đảm bảo thông tin của thẻ không bị lọt vào
        người thứ 3.</p>
    <p><strong>8. Giao kết giao dịch tại Rockylens.com</strong></p>
    <p>Khách hàng khi mua sắm tại Rockylens.com phải thực hiện các thao tác đặt hàng
        và nhận hàng.</p>
    <p>Trong các trường hợp, khách hàng có thể tra cứu thông tin giao dịch qua email
        gửi vào hộp thư khách hàng đã đăng ký với Rockylens.com hoặc tra cứu tình
        trạng giao dịch tại website này</p>
    <p>Trường hợp khách hàng muốn chỉnh sửa thông tin, khách hàng thông báo cho
        Rockylens.com qua đường dây nóng có tại website này&nbsp;hoặc chat qua
        Mesenger</p>
    <p>Chúng tôi chịu trách nhiệm về nội dung thông tin cung cấp trên trang thông
        tin điện tử Rockylens.com , thực hiện quy định của Pháp Luật về giao kết hợp
        đồng, đặt hàng, thanh toán.</p>
    <p><strong>9. Thay đổi, hủy bỏ giao dịch tại Rockylens.com</strong></p>
    <p>Trong mọi trường hợp, khách hàng đều có quyền chấm dứt giao dịch nếu đã thực
        hiện biện pháp thông báo cho Rockylens.com về việc hủy giao dịch qua đường
        dây nóng có tại website</p>
    <p><strong>10. Giải quyết hậu quả do lỗi nhập sai thông tin thương mại tại
            Rockylens.com</strong></p>
    <p>Khách hàng có trách nhiệm cung cấp thông tin đầy đủ và chính xác khi tham gia
        giao dịch tại Rockylens.com. Trong trường hợp khách hàng nhập sai thông tin
        gửi vào trang thông tin điện tử bán hàng Rockylens.com, Rockylens.com có
        quyền từ chối thực hiện giao dịch. Ngoài ra, trong mọi trường hợp, khách
        hàng đều có quyền đơn phương chấm dứt giao dịch nếu đã thực hiện biện pháp
        thông báo cho Rockylens.com qua đường dây nóng.&nbsp;Trong trường hợp sai
        thông tin phát sinh từ phía Rockylens.com mà Rockylens.com có thể chứng minh
        đó là lỗi của hệ thống hoặc từ bên thứ ba (sai giá sản phẩm, sai xuất xứ,
        …), Rockylens.com sẽ liên hệ ngay lập tức với khách hàng để thông báo trường
        hợp này và có quyền không thực hiện giao dịch bị lỗi.</p>
    <p><strong>11. Giải quyết tranh chấp</strong></p>
    <p>Bất kỳ tranh cãi, khiếu nại hoặc tranh chấp phát sinh từ hoặc liên quan đến
        giao dịch tại Rockylens.com hoặc các Quy định và Điều kiện này đều sẽ được
        giải quyết bằng hình thức thương lượng, hòa giải, trọng tài và/hoặc Tòa án
        theo Luật bảo vệ Người tiêu dùng Chương 4 về Giải quyết tranh chấp giữa
        người tiêu dùng và tổ chức, cá nhân kinh doanh hàng hóa, dịch vụ.</p>
    <p><strong>12. Quy định chấm dứt thỏa thuận</strong></p>
    <p>Trong trường hợp có bất kỳ thiệt hại nào phát sinh do việc vi phạm Quy Định
        sử dụng trang web, chúng tôi có quyền đình chỉ hoặc khóa tài khoản của quý
        khách vĩnh viễn. Nếu quý khách không hài lòng với trang web hoặc bất kỳ điều
        khoản, điều kiện, quy tắc, chính sách, hướng dẫn, hoặc cách thức vận hành
        của Rockylens.com thì biện pháp khắc phục duy nhất là ngưng làm việc với
        chúng tôi.</p>
    <p><strong>13. Luật pháp và thẩm quyền tại Việt Nam</strong></p>
    <p>Tất cả các Điều Khoản và Điều Kiện này và Hợp Đồng (và tất cả nghĩa vụ phát
        sinh ngoài hợp đồng hoặc có liên quan) sẽ bị chi phối và được hiểu theo luật
        pháp của Việt Nam. Nếu có tranh chấp phát sinh bởi các Quy định Sử dụng này,
        quý khách gửi khiếu nại lên tòa án Việt Nam để giải quyết.</p>
    <p><strong>Câu hỏi và thông tin liên hệ</strong></p>
    <p>Quý khách hàng có bất kể câu hỏi nào, vui lòng đừng ngại liên hệ với
        Rockylens.com qua số điện thoại có tại thông tin liên hệ.</p>
